// Objeto Math -- Date

// los objetos math y date son objetos utiles que se usan junto con metodos

console.log(Math.max(2, 5, 10, 30)) // --> devuelve el numero mayor dentro de un conjunto de numeros.

console.log(Math.min(2, 5, 10, 30)) // --> devuelve el numero menor dentro de un conjunto de numeros.

console.log(Math.ceil(2.1)) // --> te redondea un numero decimal hacia arriba.

console.log(Math.floor(1.8)) // --> te redondea un numero decimal hacia abajo.

console.log(Math.round(5.7)) // --> te redondea un numero decimal hacia lo mas cercano.

console.log(Math.trunc(5.7)) // --> te devuelve un numero entero si es decimal.

console.log(Math.random()) // --> te devuelve un numero aleatorio.


